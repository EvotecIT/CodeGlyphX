namespace CodeGlyphX;

/// <summary>
/// QR text encodings supported for payloads and ECI.
/// </summary>
public enum QrTextEncoding {
    /// <summary>
    /// ISO-8859-1 (Latin-1).
    /// </summary>
    Latin1,
    /// <summary>
    /// UTF-8.
    /// </summary>
    Utf8,
    /// <summary>
    /// US-ASCII.
    /// </summary>
    Ascii,
    /// <summary>
    /// ISO-8859-2.
    /// </summary>
    Iso8859_2,
    /// <summary>
    /// ISO-8859-4.
    /// </summary>
    Iso8859_4,
    /// <summary>
    /// ISO-8859-5.
    /// </summary>
    Iso8859_5,
    /// <summary>
    /// ISO-8859-7.
    /// </summary>
    Iso8859_7,
    /// <summary>
    /// ISO-8859-10.
    /// </summary>
    Iso8859_10,
    /// <summary>
    /// ISO-8859-15.
    /// </summary>
    Iso8859_15,
    /// <summary>
    /// Shift-JIS (JIS X 0208 + halfwidth katakana).
    /// </summary>
    ShiftJis
}
