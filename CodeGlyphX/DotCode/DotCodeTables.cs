namespace CodeGlyphX.DotCode;

internal static class DotCodeTables {
    internal const int Prime = 113;
    internal const int Primitive = 3;

    internal static readonly ushort[] DotPatterns = {
        0x155, 0x0AB, 0x0AD, 0x0B5, 0x0D5, 0x156, 0x15A, 0x16A, 0x1AA, 0x0AE,
        0x0B6, 0x0BA, 0x0D6, 0x0DA, 0x0EA, 0x12B, 0x12D, 0x135, 0x14B, 0x14D,
