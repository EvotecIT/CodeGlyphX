        0x1B8, 0x1C6, 0x1CC
    };

    internal static int DecodePattern(int pattern) {
        var bestValue = -1;
        var bestDistance = 10;
        for (var i = 0; i < DotPatterns.Length; i++) {
            var distance = PopCount(pattern ^ DotPatterns[i]);
            if (distance < bestDistance) { bestDistance = distance; bestValue = i; }
        }
        return bestValue;
    }

    private static int PopCount(int value) {
        var count = 0;
        while (value != 0) { value &= value - 1; count++; }
        return count;
    }
}
