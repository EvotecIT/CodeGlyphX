param(
    [Parameter(Mandatory)]
    [string] $DictionaryPath,

    [string] $OutputPath = (Join-Path $PSScriptRoot '..\CodeGlyphX\Gs1\Gs1ApplicationIdentifierCatalog.Generated.cs'),

    [string] $CodeListOutputPath = (Join-Path $PSScriptRoot '..\CodeGlyphX\Gs1\Gs1Validator.CodeLists.Generated.cs'),

    [string] $SourceCommit = '8ad003f83a4ba6c2179ce15d578fae27d6580909'
)

$ErrorActionPreference = 'Stop'

$resolvedDictionary = (Resolve-Path -LiteralPath $DictionaryPath).Path
$releaseLine = Get-Content -LiteralPath $resolvedDictionary -TotalCount 20 |
    Where-Object { $_ -match '^# Release:\s*(?<release>\S+)' } |
    Select-Object -First 1

if (-not $releaseLine -or $releaseLine -notmatch '^# Release:\s*(?<release>\S+)') {
    throw "The GS1 dictionary release could not be read from '$resolvedDictionary'."
}

$release = $Matches.release
$definitions = [System.Collections.Generic.List[object]]::new()

foreach ($line in Get-Content -LiteralPath $resolvedDictionary) {
    if ($line -notmatch '^\s*\d') {
        continue
    }

    $parts = $line -split '#', 2
    $body = $parts[0].Trim()
    $title = if ($parts.Count -eq 2) { $parts[1].Trim() } else { '' }

    if ($body -notmatch '^(?<ais>\d+(?:-\d+)?)\s+(?<rules>.+)$') {
        throw "Unrecognized dictionary row: $line"
    }

    $aiExpression = $Matches.ais
    $tokens = $Matches.rules -split '\s+'
    $flags = ''
    $formats = [System.Collections.Generic.List[string]]::new()
    $attributes = [System.Collections.Generic.List[string]]::new()

    foreach ($token in $tokens) {
        if ($token -match '^[*?]+$') {
            $flags = $token
        } elseif ($token -match '^\[?[NXYZ]') {
            $formats.Add($token)
        } else {
            $attributes.Add($token)
        }
    }

    if ($formats.Count -eq 0) {
        throw "Dictionary row has no data format: $line"
    }

    $ais = if ($aiExpression -match '^(?<start>\d+)-(?<end>\d+)$') {
        $width = $Matches.start.Length
        $start = [int] $Matches.start
        $end = [int] $Matches.end
        if ($end -lt $start -or $Matches.end.Length -ne $width) {
            throw "Invalid AI range '$aiExpression'."
        }
        $start..$end | ForEach-Object { $_.ToString("D$width") }
    } else {
        @($aiExpression)
    }

    foreach ($ai in $ais) {
        $definitions.Add([pscustomobject]@{
            Ai = $ai
            Title = $title
            Format = $formats -join ' '
            PredefinedLength = $flags.Contains('*')
            DigitalLinkDataAttribute = $flags.Contains('?')
            Attributes = $attributes -join ' '
        })
    }
}

if ($definitions.Count -eq 0) {
    throw "No GS1 Application Identifiers were parsed from '$resolvedDictionary'."
}

function ConvertTo-CSharpString([string] $Value) {
    return $Value.Replace('\', '\\').Replace('"', '\"')
}

$lines = [System.Collections.Generic.List[string]]::new()
$lines.Add('// <auto-generated />')
$lines.Add('// Generated from the GS1 Barcode Syntax Dictionary.')
$lines.Add("// Release: $release")
$lines.Add("// Source commit: $SourceCommit")
$lines.Add('// Source: https://github.com/gs1/gs1-syntax-dictionary')
$lines.Add('')
$lines.Add('namespace CodeGlyphX.Gs1Data;')
$lines.Add('')
$lines.Add('public static partial class Gs1ApplicationIdentifierCatalog {')
$lines.Add("    /// <summary>Gets the GS1 Barcode Syntax Dictionary release used by this catalog.</summary>")
$lines.Add(('    public const string Release = "{0}";' -f $release))
$lines.Add('')
$lines.Add('    private static Gs1ApplicationIdentifier[] CreateDefinitions() {')
$lines.Add('        return new[] {')

foreach ($definition in $definitions) {
    $ai = ConvertTo-CSharpString $definition.Ai
    $title = ConvertTo-CSharpString $definition.Title
    $format = ConvertTo-CSharpString $definition.Format
    $attributes = ConvertTo-CSharpString $definition.Attributes
    $predefined = $definition.PredefinedLength.ToString().ToLowerInvariant()
    $digitalLink = $definition.DigitalLinkDataAttribute.ToString().ToLowerInvariant()
    $lines.Add(('            new Gs1ApplicationIdentifier("{0}", "{1}", "{2}", {3}, {4}, "{5}"),' -f $ai, $title, $format, $predefined, $digitalLink, $attributes))
}

$lines.Add('        };')
$lines.Add('    }')
$lines.Add('}')

$resolvedOutput = [System.IO.Path]::GetFullPath($OutputPath)
$outputDirectory = [System.IO.Path]::GetDirectoryName($resolvedOutput)
[System.IO.Directory]::CreateDirectory($outputDirectory) | Out-Null
[System.IO.File]::WriteAllLines($resolvedOutput, $lines, [System.Text.UTF8Encoding]::new($false))

$dictionaryRoot = [System.IO.Path]::GetDirectoryName($resolvedDictionary)
$packageTypeSource = Join-Path $dictionaryRoot 'src\lint_packagetype.c'
if (-not (Test-Path -LiteralPath $packageTypeSource)) {
    throw "Package type source was not found at '$packageTypeSource'. Use a checkout of the tagged GS1 dictionary repository."
}

$packageTypeText = [System.IO.File]::ReadAllText($packageTypeSource)
$arrayStart = $packageTypeText.IndexOf('static const char packagetypes[][4]', [System.StringComparison]::Ordinal)
$arrayEnd = if ($arrayStart -ge 0) { $packageTypeText.IndexOf('};', $arrayStart, [System.StringComparison]::Ordinal) } else { -1 }
if ($arrayStart -lt 0 -or $arrayEnd -lt 0) {
    throw "The PackageTypeCode array could not be parsed from '$packageTypeSource'."
}

$packageTypes = [regex]::Matches($packageTypeText.Substring($arrayStart, $arrayEnd - $arrayStart), '"(?<code>[^"]+)"') |
    ForEach-Object { $_.Groups['code'].Value }

$codeListLines = [System.Collections.Generic.List[string]]::new()
$codeListLines.Add('// <auto-generated />')
$codeListLines.Add('// Generated from the GS1 Barcode Syntax Dictionary reference linters.')
$codeListLines.Add("// Release: $release")
$codeListLines.Add("// Source commit: $SourceCommit")
$codeListLines.Add('')
$codeListLines.Add('namespace CodeGlyphX.Gs1Data;')
$codeListLines.Add('')
$codeListLines.Add('public static partial class Gs1Validator {')
$codeListLines.Add('    private static readonly string[] PackageTypes = {')
foreach ($packageType in $packageTypes) {
    $codeListLines.Add(('        "{0}",' -f (ConvertTo-CSharpString $packageType)))
}
$codeListLines.Add('    };')
$codeListLines.Add('}')

$resolvedCodeListOutput = [System.IO.Path]::GetFullPath($CodeListOutputPath)
$codeListOutputDirectory = [System.IO.Path]::GetDirectoryName($resolvedCodeListOutput)
[System.IO.Directory]::CreateDirectory($codeListOutputDirectory) | Out-Null
[System.IO.File]::WriteAllLines($resolvedCodeListOutput, $codeListLines, [System.Text.UTF8Encoding]::new($false))

Write-Host "Generated $($definitions.Count) GS1 Application Identifiers and $($packageTypes.Count) package type codes from release $release."
